class Solution {
public:
    bool isValidSudoku(vector<vector<char>> &board) {
        unsigned long oneSize = board.size();
        unsigned long twoSize = board.at(0).size();
        // ä»¥ä¸‹çš„ y è¡¨ç¤ºæ»¡è¶³ï¼Œn è¡¨ç¤ºä¸æ»¡è¶³ï¼Œå…¶ä»– è¡¨ç¤ºæœªçŸ¥
        long *cacheRow = new long[9]{0};              // è¡Œ
        long *cacheColumn = new long[9]{0};           // åˆ—
        long cacheTian[3][3] = {0};             // ç”°å­—

        /**
         * å°†charå‹æ•°ï¼Œè½¬åŒ–ä¸º å¯¹åº”çš„2è¿›åˆ¶ï¼Œå¯¹åº”çš„åè¿›åˆ¶
         * eg: char 2  ->  äºŒè¿›åˆ¶ 10 ->  åè¿›åˆ¶ 2
         * eg: char 4  ->  äºŒè¿›åˆ¶ 1000 ->  åè¿›åˆ¶ 8
         */
        function<long(char &)> toInt;
        toInt = [](char &str) -> long {
            if (str == '.') {
                return 0;
            }
            long one = 1;
            return one << ((str - 48) - 1);
        };

        for (int i = 0; i < oneSize; ++i) {
            for (int j = 0; j < twoSize; ++j) {
                long temp = toInt(board[i][j]);
                if (temp == 0) {
                    continue;
                }
                /**
                 * eg: 11001000 | 1000 å¦‚æœä¸åŸå€¼ç›¸ç­‰ï¼Œè¯´æ˜ 1000 å·²ç»å­˜åœ¨
                 */
                long i1 = cacheRow[i];
                cacheRow[i] |= temp;
                if (cacheRow[i] == i1) {
                    return false;
                }

                long i2 = cacheColumn[j];
                cacheColumn[j] |= temp;
                if ((cacheColumn[j]) == i2) {
                    return false;
                }

                long i3 = cacheTian[i / 3][j / 3];
                cacheTian[i / 3][j / 3] |= temp;
                if ((cacheTian[i / 3][j / 3]) == i3) {
                    return false;
                }
            }
        }
        return true;
    }
};
